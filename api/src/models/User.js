const mongoose= require('mongoose')

const userSchema= mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    surname: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    avatar: {
        type: String,
        required: false
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
})



userSchema.set('toJSON',{
virtuals:true,
});

exports.User = mongoose.model('User', userSchema)